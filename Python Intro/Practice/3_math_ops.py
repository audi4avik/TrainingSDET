import math

print(12 + 3)
print(6 - 5)
print(2 * 13)
print(10 / 3)
print(10 % 3)  # integer remainder
print(13 // 6)  # floor division
print(12 ** 3)  # exponent

print(2 + 3 * 3 / 2 ** 2)  # operator precedence -> parenthesis > exponential > div or multi > add or sub

num = 2.9
print(abs(num))
print(math.ceil(num))  # nearest higher int >= x
print(math.floor(num))  # nearest lower int <= x
